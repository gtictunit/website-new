                    The church consequently adopted three of the critically unreached people groups in Nigeria on the Joshua Project. Three missionaries were selected and placed on special sponsorship. This gave rise to the program we now refer to as Special Missionary Sponsorship Program(SMSP). Each of the missionaries was placed on a stipend of five thousand naira per month. Since then we have had and still have several missionaries in critically unreached areas under our sponsorship. <br><br>

                    At this juncture, the Pastor encouraged individuals and teams in the church to get personally involved in mission support. Everyone was made to realize that this is an acceptable service to God, which has both temporal and eternal rewards. Any amount was acceptable as long as it will be given on a regular basis. Over 100 people made monthly pledges of between 50 and 5000 naira. In addition, many teams (Ushers, Choir, Children, Deacons, Singles, etc.) in the church decided to sponsor one or more missionaries in the PMSP. <br><br>

                    Private businesses, Schools and other enterprises provided corporate contributions. Together with the tithe of tithes, more resources became available for mission support. In order to coordinate all these contributions and enhance the effective and fruitful use of the resources, the Mission Support Clearing House (MSCH) was set up. <br><br>

